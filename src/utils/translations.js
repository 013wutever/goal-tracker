// Helper functions
export const getTranslation = (language, path) => {
  return path.split('.').reduce((obj, key) => obj?.[key], translations[language]) || path;
};

export const capitalize = (str) => {
  return str.charAt(0).toUpperCase() + str.slice(1);
};
